$config['admin_email']                = "admin@example.com"; // Admin Email, admin@example.com
$config['default_group']              = 'members';           // Default group, use name
$config['admin_group']                = 'admin';             // Default administrators group, use name
$config['identity']                   = 'us_usuario';        // Este es nombre para la validacion You can use any unique column in your table as identity column. The values in this column, alongside password, will be used for login purposes
